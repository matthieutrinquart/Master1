--  ==============================================================*/
--   Table : CLIENTS                                              */
--  ==============================================================*/
create table CLIENTS (
CODECLIENT           INT8                 not null,
NOMCLIENT            VARCHAR(35)          null,
PRECLIENT       	 VARCHAR(35)          null,
ADRCLIENT            VARCHAR(50)          null,
