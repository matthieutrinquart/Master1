--  ==============================================================*/
--   Table : COMMANDES                                            */
--  ==============================================================*/
create table COMMANDES (
NUMCMDE              INT8                 not null,
CODECLIENT           INT8                 null,
CODEVENDEUR          INT8                 null,
DATELIV              DATE                 null,
DATECMDE             DATE                 null,
TOTHT                FLOAT8               null,
TOTTVA               FLOAT8               null,
